package main
import "fmt"

var yes int // ! 声明全局变量yes 但是没有赋值

func init() {
	// ! 除了在全局变量初始化函数，还可以使用init函数来初始化
	 yes  = 1 // 对yes赋值，这个会首先被初始化 ，本质上这是个语句
	fmt.Println("yes") // ! 可以看见yes比1先输出来
}
func main() {
	fmt.Println(yes)
}
